LLM_SYSTEM = (
    "You extract battery/cell datasheet parameters. "
    "Return ONLY a JSON object, no prose, no markdown fences. "
    "Use null for anything not present. Convert units to the schema "
    "(capacity Ah, energy Wh, internal resistance milliohm, currents A, weight g). "
    "Do not invent values."
)


def extract_llm(text, model):
    """Use the Anthropic API. Requires ANTHROPIC_API_KEY and the anthropic SDK."""
    import anthropic
    client = anthropic.Anthropic()
    prompt = (
        "Extract these fields from the datasheet text below.\n"
        f"Return exactly this JSON shape (keys fixed):\n{json.dumps(empty_spec(), indent=2)}\n\n"
        "- dimensions_mm: string like '207.5*173.7*72'\n"
        "- operating_temp_C: string range like '-20℃ ~ 60℃'\n"
        "- cycle_life: integer\n"
        "- weight_g: grams (number)\n"
        "- max_charge_current_A / max_discharge_current_A: amperes (number)\n"
        "- If multiple values exist, pick the nominal single-cell value.\n\n"
        f"DATASHEET TEXT:\n{text[:18000]}"
    )
    msg = client.messages.create(
        model=model, max_tokens=1024, system=LLM_SYSTEM,
        messages=[{"role": "user", "content": prompt}],
    )
    raw = "".join(b.text for b in msg.content if getattr(b, "type", "") == "text")
    raw = raw.strip().removeprefix("```json").removeprefix("```").removesuffix("```").strip()
    data = json.loads(raw)

    spec = empty_spec()
    for k in FIELDS:
        if k in data and data[k] is not None:
            spec[k] = data[k]
    return spec


def merge_specs(primary, secondary):
    """Fill primary's None fields from secondary."""
    out = dict(primary)
    for k in FIELDS:
        if out.get(k) is None and secondary.get(k) is not None:
            out[k] = secondary[k]
    return out

# core extraction (used by both endpoints)
# ==========================================================================
def run_extraction(source, use_llm=False, llm_model=None):
    """Acquire text and extract specs. Returns a result dict."""
    llm_model = llm_model or CONFIG["llm_model"]
    text, table_pairs, kind = acquire_text(source)

    spec, matches = extract_regex(text, table_pairs)   # deterministic baseline
    method = "regex"
    llm_error = None

    if use_llm:
        try:
            llm_spec = extract_llm(text, llm_model)
            spec = merge_specs(llm_spec, spec)  # prefer LLM, backfill from regex
            method = "llm+regex"
        except Exception as e:
            llm_error = str(e)
            method = "regex (llm failed)"

    found = sum(1 for k in FIELDS if spec.get(k) is not None)
    result = {
        "specs": spec,
        "fields_found": found,
        "fields_total": len(FIELDS),
        "input_kind": kind,
        "method": method,
    }
    if llm_error:
        result["llm_error"] = llm_error
    return result


