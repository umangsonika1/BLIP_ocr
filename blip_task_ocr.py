@app.route("/datasheet-extract", methods=["POST"])
def datasheet_extract():
    """Extract specs from an uploaded datasheet PDF (multipart/form-data)."""
    tmp_path = None
    try:
        if "file" not in request.files:
            return jsonify({
                "success": False,
                "error": "No file uploaded. Expected 'file' parameter with a PDF datasheet",
            }), 400

        file = request.files["file"]
        if not file.filename:
            return jsonify({"success": False, "error": "Empty filename"}), 400
        if not file.filename.lower().endswith(".pdf"):
            return jsonify({
                "success": False,
                "error": "File must be PDF format (.pdf extension)",
            }), 400

        use_llm = _truthy(request.form.get("llm", CONFIG["use_llm_default"]))

        print(f"\n[ENDPOINT] /datasheet-extract called")
        print(f"[ENDPOINT] File: {file.filename} | llm: {use_llm}")

        tmp = tempfile.NamedTemporaryFile(suffix=".pdf", delete=False)
        tmp_path = tmp.name
        file.save(tmp_path)
        tmp.close()

        result = run_extraction(tmp_path, use_llm=use_llm)
        print(f"[ENDPOINT] Extracted {result['fields_found']}/{result['fields_total']} fields")

        response = format_response(file.filename, result)
        print(f"[ENDPOINT] Processing complete")
        return jsonify(response), 200

    except Exception as e:
        print(f"[ENDPOINT ERROR] {str(e)}")
        return jsonify({
            "success": False, "error": str(e),
            "timestamp": datetime.datetime.now().isoformat(),
        }), 500
    finally:
        if tmp_path:
            try:
                os.unlink(tmp_path)
            except OSError:
                pass


