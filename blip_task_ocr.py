def main():
    use_ngrok = os.getenv("USE_NGROK", "false").lower() == "true"
    port = int(os.getenv("PORT", 5011))
    try:
        from IPython import get_ipython
        if "google.colab" in str(get_ipython()):
            print("[INFO] Detected Colab environment; setting up dependencies...")
            import subprocess
            subprocess.run(["pip", "install", "flask", "pyngrok"], check=True)
            use_ngrok = True
    except Exception:
        pass  # Not running in Colab

    if use_ngrok:
        auth_token = os.getenv("NGROK_AUTH_TOKEN")
        if not auth_token:
            raise ValueError(
                "NGROK_AUTH_TOKEN not set. Get one from https://dashboard.ngrok.com/get-started/your-authtoken"
            )

        ngrok.set_auth_token(auth_token)
        print(f"[INFO] Ngrok auth token set: {auth_token[:5]}... (masked)")

        try:
            public_url = ngrok.connect(addr=str(port), proto="http")
            print(f"[INFO] Public URL for testing in Postman: {public_url}")
        except Exception as e:
            print(f"[WARNING] Port {port} failed: {e}, retrying on port {port + 1}")
            port += 1
            public_url = ngrok.connect(addr=str(port), proto="http")
            print(f"[INFO] Public URL for testing in Postman: {public_url}")
        flask_thread = threading.Thread(target=run_flask, args=(port,), daemon=True)
        flask_thread.start()
        flask_thread.join()
    else:
        print(f"[INFO] Running Flask on port {port} without ngrok (production mode)")
        run_flask(port)

