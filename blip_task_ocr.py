def is_url(s):
    return s.lower().startswith(("http://", "https://"))


def acquire_text(source):
    """Return (text, table_pairs, kind) for a local path or URL.

    table_pairs: list of (label, value) tuples from any 2-column table rows.
    kind: 'pdf_file' | 'pdf_url' | 'html_url'
    """
    if not is_url(source):
        text, pairs = text_from_pdf_path(source)
        return text, pairs, "pdf_file"

    headers = {"User-Agent": "Mozilla/5.0 (datasheet-extractor)"}
    resp = requests.get(source, headers=headers, timeout=30)
    resp.raise_for_status()
    ctype = resp.headers.get("Content-Type", "").lower()

    is_pdf = (
        "application/pdf" in ctype
        or source.lower().split("?")[0].endswith(".pdf")
        or resp.content[:5] == b"%PDF-"
    )
    if is_pdf:
        text, pairs = text_from_pdf_bytes(resp.content)
        return text, pairs, "pdf_url"
    text, pairs = text_from_html(resp.text)
    return text, pairs, "html_url"


def _value_score(cell):
    """Heuristic: how much a cell looks like a spec VALUE (vs a label/remark).
    Higher = more value-like. Short cells with a number+unit score highest;
    long prose scores low/negative. Ranges (-20~60) score high too."""
    s = str(cell).strip()
    if not s:
        return -100
    words = len(s.split())
    score = 0
    if re.search(r"\d", s):
        score += 2
    if re.search(r"\d\s*(?:v|ah|wh|mω|mohm|w|mm|℃|°c|kg|g|n|nm|a)\b", s.lower()):
        score += 3
    # a range like -20~60 or -20℃~60℃ is a strong value signal
    if re.search(r"-?\d.*(?:~|to|\u2013|\u2014).*-?\d", s):
        score += 3
    score -= max(0, words - 3)        # penalize prose
    if words > 6:
        score -= 5
    return score


def _pairs_from_pdf(pdf):
    """Extract (label, value) pairs from 2+ column tables in a pdfplumber pdf.

    For rows with 3+ columns (e.g. Item | Standard | Remarks), choose the
    column that looks most like a value, not blindly the last one. Also emit
    every other candidate as a fallback so coercion can pick a valid one.
    """
    pairs = []
    for page in pdf.pages:
        for table in page.extract_tables() or []:
            for row in table:
                cells = [str(c).strip() for c in row if c and str(c).strip()]
                if len(cells) < 2:
                    continue
                label = cells[0]
                candidates = cells[1:]
                best = max(candidates, key=_value_score)
                pairs.append((label, best))
                for c in candidates:
                    if c != best:
                        pairs.append((label, c))
    return pairs


def text_from_pdf_path(path):
    pages = []
    with pdfplumber.open(path) as pdf:
        pairs = _pairs_from_pdf(pdf)
        for i, page in enumerate(pdf.pages):
            txt = page.extract_text() or ""
            if len(txt.strip()) >= 40:
                pages.append(txt)
            else:
                imgs = convert_from_path(path, dpi=CONFIG["ocr_dpi"],
                                         first_page=i + 1, last_page=i + 1)
                pages.append(ocr_image(imgs[0]) if imgs else "")
    return "\n".join(pages), pairs


def text_from_pdf_bytes(data):
    pages = []
    ocr_pages = []
    with pdfplumber.open(io.BytesIO(data)) as pdf:
        pairs = _pairs_from_pdf(pdf)
        for i, page in enumerate(pdf.pages):
            txt = page.extract_text() or ""
            if len(txt.strip()) >= 40:
                pages.append(txt)
            else:
                pages.append(None)
                ocr_pages.append(i + 1)
    for pageno in ocr_pages:
        imgs = convert_from_bytes(data, dpi=CONFIG["ocr_dpi"],
                                  first_page=pageno, last_page=pageno)
        pages[pageno - 1] = ocr_image(imgs[0]) if imgs else ""
    return "\n".join(p or "" for p in pages), pairs


def text_from_html(html):
    soup = BeautifulSoup(html, "html.parser")
    for tag in soup(["script", "style", "noscript"]):
        tag.decompose()
    pairs = []
    lines = []
    for table in soup.find_all("table"):
        for tr in table.find_all("tr"):
            cells = [c.get_text(" ", strip=True) for c in tr.find_all(["td", "th"])]
            cells = [c for c in cells if c]
            if len(cells) >= 2:
                # pick the most value-like cell, not blindly the last
                best = max(cells[1:], key=_value_score)
                pairs.append((cells[0], best))
                for c in cells[1:]:
                    if c != best:
                        pairs.append((cells[0], c))
            if cells:
                lines.append(" : ".join(cells))
    text = soup.get_text("\n", strip=True) + "\n" + "\n".join(lines)
    return text, pairs


def ocr_image(pil_image):
    img = pil_image.convert("L")
    best = ""
    for psm in (4, 6, 11):
        out = pytesseract.image_to_string(img, config=f"--oem 3 --psm {psm}")
        if len(out.strip()) > len(best.strip()):
            best = out
    return best

# regex extractor (offline baseline)
# ==========================================================================
_NUM = r"[-+]?\d+(?:[.,]\d+)?"


def _looks_like_range(s):
    """True for things like '-20~60', '-20C to 60C' that should stay strings."""
    return bool(re.search(r"-?\d.*(?:~|to|\u2013|\u2014).*\d", s))


def _looks_like_dimensions(s):
    """True for 'L*W*H' style dimension strings: '174*72*207' or '174x72x207'."""
    return bool(re.search(r"\d+(?:[.,]\d+)?\s*[x*×]\s*\d+(?:[.,]\d+)?\s*[x*×]\s*\d+", s.lower()))


# Per-field-suffix sanity bounds, used to reject values that matched a label
# but are clearly the wrong number (e.g. a C-rate, a percentage, stray text).
_BOUNDS = {
    "_V": (0.3, 6.0),          # single-cell voltage
    "_Ah": (1.0, 2000.0),
    "_Wh": (1.0, 20000.0),
    "_mOhm": (0.01, 50.0),     # cell internal resistance in milliohm
    "_A": (0.1, 2000.0),       # charge/discharge current
    "_W": (10.0, 100000.0),    # power; floor raised so stray '1' is rejected
    "_g": (50.0, 50000.0),     # cell mass in grams
    "_mm": (0.5, 1000.0),
    "_C": (-60.0, 150.0),      # a temperature
}


def _bounds_for(key):
    # longest suffix wins (so _mOhm beats shorter suffixes, etc.)
    for suffix in sorted(_BOUNDS, key=len, reverse=True):
        if key.endswith(suffix):
            return _BOUNDS[suffix]
    return None


def _is_junk_value(raw, key=None):
    """Reject values that are obviously not a spec value."""
    low = raw.lower().strip()
    if not low:
        return True
    if len(low.split()) > 6:
        return True
    is_temp = bool(key) and key.endswith("_C")
    # P-rate always junk (e.g. '0.5p')
    if re.match(r"^\d+(?:[.,]\d+)?\s*p\b", low):
        return True
    # C-rate like '0.5c' / '1c' — but NOT a Celsius temperature. Skip for _C.
    if not is_temp:
        if re.match(r"^\d+(?:[.,]\d+)?c\b", low) and "°" not in raw and "℃" not in raw:
            return True
    # bare percentage
    if re.match(r"^\d+(?:[.,]\d+)?\s*%$", low):
        return True
    return False


def _coerce_value(key, raw_value):
    """Coerce + VALIDATE a raw value for a given field key.
    Returns the typed value, or None if the value is junk / out of range."""
    ftype = SCHEMA[key]["type"]
    raw_value = str(raw_value).strip(" :\t|").strip()
    if not raw_value or _is_junk_value(raw_value, key):
        return None

    # dimensions field: keep the L*W*H string verbatim
    if key == "dimensions_mm":
        if _looks_like_dimensions(raw_value):
            return re.sub(r"\s+", "", raw_value)[:60]
        return None

    # Range strings stay as text (temperatures, etc.), only for str / _C fields.
    if _looks_like_range(raw_value):
        if ftype == "str" or key.endswith("_C"):
            return re.sub(r"\s+", " ", raw_value)[:80]
        return None

    if ftype in ("num", "int"):
        m = re.search(_NUM, raw_value)
        if not m:
            return None
        try:
            n = float(m.group(0).replace(",", "."))
        except ValueError:
            return None
        rng = _bounds_for(key)
        if rng and not (rng[0] <= n <= rng[1]):
            return None
        return int(round(n)) if ftype == "int" else n

    # string field: keep short, single-value-ish strings only
    cleaned = re.sub(r"\s+", " ", raw_value).strip()
    if not cleaned:
        return None
    if key in ("model", "manufacturer") and len(cleaned) < 2:
        return None
    return cleaned[:80]


def _build_alias_index():
    """Map each alias -> field key, longest alias first so specific labels win
    (e.g. 'absolute charging voltage' before 'charging voltage')."""
    pairs = []
    for key, spec in SCHEMA.items():
        for alias in spec["aliases"]:
            pairs.append((alias.lower(), key))
    pairs.sort(key=lambda p: len(p[0]), reverse=True)
    return pairs


_ALIAS_INDEX = _build_alias_index()


def _split_explicit(line):
    """If the line has an explicit separator, return (label, value) else None."""
    for sep in (" : ", "\t", " = "):
        if sep in line:
            left, right = line.split(sep, 1)
            if left.strip() and right.strip():
                return left.strip(), right.strip()
    if ":" in line:
        left, right = line.split(":", 1)
        if left.strip() and right.strip() and not left.strip()[-1].isdigit():
            return left.strip(), right.strip()
    return None


def _alias_matches(llabel, alias):
    """Generic, short aliases (<=7 chars or single word) must match the label
    EXACTLY, to avoid stealing from longer labels. Longer aliases may match by
    startswith/contains."""
    if llabel == alias:
        return True
    if len(alias) <= 7 or " " not in alias:
        return False
    return llabel.startswith(alias) or alias in llabel


def extract_from_pairs(pairs, spec, matches):
    """Assign values from clean (label, value) pairs (e.g. PDF table cells)."""
    for label, value in pairs:
        if label is None or value is None:
            continue
        llabel = str(label).strip().lower()
        sval = str(value).strip()
        if not llabel or not sval:
            continue
        for alias, key in _ALIAS_INDEX:
            if spec.get(key) is not None:
                continue
            if _alias_matches(llabel, alias):
                coerced = _coerce_value(key, sval)
                if coerced is not None:
                    spec[key] = coerced
                    matches[key] = f"{str(label).strip()[:40]} => {sval[:40]}"
                break
    return spec, matches


def extract_from_text(text, spec, matches):
    """Alias-anchored scan over free text lines."""
    for raw in text.splitlines():
        line = raw.strip()
        if not line:
            continue

        ex = _split_explicit(line)
        if ex:
            extract_from_pairs([ex], spec, matches)
            continue

        lline = line.lower()
        for alias, key in _ALIAS_INDEX:
            if spec.get(key) is not None:
                continue
            if len(alias) <= 7 or " " not in alias:
                continue
            if lline.startswith(alias):
                remainder = line[len(alias):].strip(" :=\t-")
                coerced = _coerce_value(key, remainder)
                if coerced is not None:
                    spec[key] = coerced
                    matches[key] = f"{alias} => {remainder[:40]}"
                break
    return spec, matches


def extract_regex(text, table_pairs=None):
    """Generic label->value extractor over the full SCHEMA."""
    spec = empty_spec()
    matches = {}

    if table_pairs:
        extract_from_pairs(table_pairs, spec, matches)
    extract_from_text(text, spec, matches)

    low = text.lower()

    # chemistry fallback by content pattern
    if spec.get("chemistry") is None:
        m = re.search(r"(lifepo4|lfp|li-?ion lifepo4|nmc|nca|lco|lto|lithium iron phosphate)", low)
        if m:
            spec["chemistry"] = m.group(1)
    if isinstance(spec.get("chemistry"), str):
        c = spec["chemistry"].lower()
        norm = ("LFP" if ("lifepo4" in c or "lfp" in c or "iron phosphate" in c)
                else "NMC" if "nmc" in c else "NCA" if "nca" in c
                else "LCO" if "lco" in c else "LTO" if "lto" in c
                else spec["chemistry"].upper())
        spec["chemistry"] = norm

    # manufacturer fallback
    if spec.get("manufacturer") is None:
        m = re.search(r"\b(eve|catl|calb|byd|samsung sdi|lg energy|lg chem|panasonic|gotion|lishen)\b", low)
        if m:
            spec["manufacturer"] = m.group(1).upper()

    # capacity / energy fallbacks
    if spec.get("nominal_capacity_Ah") is None:
        if spec.get("min_capacity_Ah") is not None:
            spec["nominal_capacity_Ah"] = spec["min_capacity_Ah"]
        else:
            m = re.search(r"(\d{2,4}(?:[.,]\d+)?)\s*ah\b", low)
            if m:
                v = _coerce_value("nominal_capacity_Ah", m.group(0))
                if v is not None:
                    spec["nominal_capacity_Ah"] = v
    if spec.get("nominal_energy_Wh") is None and spec.get("min_energy_Wh") is not None:
        spec["nominal_energy_Wh"] = spec["min_energy_Wh"]

    # dimensions fallback: look for an L*W*H pattern anywhere
    if spec.get("dimensions_mm") is None:
        m = re.search(r"\d+(?:[.,]\d+)?\s*[x*×]\s*\d+(?:[.,]\d+)?\s*[x*×]\s*\d+(?:[.,]\d+)?", text)
        if m:
            v = _coerce_value("dimensions_mm", m.group(0))
            if v is not None:
                spec["dimensions_mm"] = v

    # operating_temp_C fallback: derive from a min/max pair if available
    if spec.get("operating_temp_C") is None:
        lo, hi = spec.get("min_operating_temp_C"), spec.get("max_operating_temp_C")
        if lo is not None and hi is not None:
            spec["operating_temp_C"] = f"{lo}℃ ~ {hi}℃"

    return spec, matches

# LLM extractor (optional, Anthropic API)
